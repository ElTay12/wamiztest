<?php
namespace App\Entity;

class Newsletter {

    /**
     * @var string
     */
    private $title;

    /**
     * @var body
     */
    private $body;

    public function getTitle(): ?string
    {
        return $this->title;
    }

    public function setTitle(string $title): self
    {
        $this->title = $title;

        return $this;
    }

    public function getBody(): ?string
    {
        return $this->body;
    }

    public function setBody(string $body): self
    {
        $this->body = $body;

        return $this;
    }
}